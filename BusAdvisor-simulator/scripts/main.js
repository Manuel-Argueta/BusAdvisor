    confirmBusBtn.addEventListener("click", () => {
        busCreationForm.style.display = "none";
        confirmBusBtn.style.display = "none";
        createNewBus();
    });
    startSimBtn.addEventListener("click", () => { 
        simulationMap.renderMap();
        animateAllBuses();

    });
    stopSimBtn.addEventListener("click", () => {

    });


